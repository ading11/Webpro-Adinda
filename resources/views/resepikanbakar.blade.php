        <div class="col-lg-12">
            <h2>Cara Memasak:</h2>
            <ol>
                <li>Bersihkan ikan, lumuri dengan jeruk nipis dan garam. Diamkan selama 15 menit.</li>
                <li>Siapkan bumbu marinasi, campur semua bahan menjadi satu.</li>
                <li>Oleskan bumbu marinasi ke seluruh bagian ikan, termasuk perutnya. Diamkan selama 30 menit.</li>
                <li>Panaskan grill atau panggangan, bungkus ikan dengan daun pisang.</li>
                <li>Bakar ikan di atas panggangan selama 20-30 menit atau sampai ikan matang sempurna.</li>
                <li>Sajikan ikan bakar dengan nasi hangat.</li>
            </ol>
